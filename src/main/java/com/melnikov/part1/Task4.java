package com.melnikov.part1;

public class Task4 {
    public static void main(String[] args) {
        double a = 5.1;
        double b = 4.3;

        System.out.println("a: " + a + "; b: " + b);

        System.out.println("a > b: " + (a > b) + "\n"
                            + "a < b: " + (a < b) + "\n"
                            + "a >= b: " + (a >= b) + "\n"
                            + "a <= b: " + (a <= b) + "\n"
                            + "a != b: " + (a != b) + "\n"
                            + "a == b: " + (a == b));
    }
}
